public class ArithmeticOp {
    public static void main(String args[]) {
        // Arithmetic Operators
        int a = 10;
        int b = 5;

        int add = a + b;
        int diff = a - b;
        int mul = a * b;
        int div = a / b;
        int rem = a % b;

        System.out.println(add);
        System.out.println(diff);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(rem);
    }
}