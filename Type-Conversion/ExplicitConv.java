public class ExplicitConv {
    public static void main(String abhi[]){
        // int to byte
        int a = 300;
        byte b = (byte)a;
        System.out.println(a + " " + b);

        //int to character
        int i = 97;
        char ch = (char)i;
        System.out.println(i + " " + ch);
    }
}